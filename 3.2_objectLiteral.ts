//Literal type
// Define the object literal type 
let car: { make: string; year: number; isElectric: boolean; }; 
// Initialize the object with valid properties 
car = { make: 'Tesla', year: 2024, isElectric: true };

console.log("Car make: " + car.make);
console.log("Car make year: " + car.year);
console.log("is Car electric: " + car.isElectric);

//an object literal is a way to define and create objects with a specific 
//set of properties and values. It's a concise syntax for initializing objects 
//and their properties directly. 